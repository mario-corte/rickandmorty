import SwiftUI

extension Color {
    static let primary = Color("RAMGreen")
    static let secondary = Color("RAMPurple")
    static let linkedin = Color("LinkedIn")
    
    static let background = Color(uiColor: UIColor.systemBackground)
    
    static let darkText = Color(uiColor: UIColor.darkText)
    static let lightText = Color(uiColor: UIColor.lightText)
    static let placeholderText = Color(uiColor: UIColor.placeholderText)
    
    static let label = Color(uiColor: UIColor.label)
    static let secondaryLabel = Color(uiColor: UIColor.secondaryLabel)
    static let tertiaryLabel = Color(uiColor: UIColor.tertiaryLabel)
    static let quaternaryLabel = Color(uiColor: UIColor.quaternaryLabel)
    
    static let fill = Color(uiColor: UIColor.systemFill)
    static let secondaryFill = Color(uiColor: UIColor.secondarySystemFill)
    static let tertiaryFill = Color(uiColor: UIColor.tertiarySystemFill)
    static let quaternaryFill = Color(uiColor: UIColor.quaternarySystemFill)
}
